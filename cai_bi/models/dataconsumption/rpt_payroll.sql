        te.item_id,
        te.labor_gl_batch_key,
        te.location_id,
        te.location_key,
        te.non_billnu_acct_key,
        te.non_billu_acct_key,
        te.stat_gl_batch_key,
        te.stat_journal_key,
        te.task_id,
